import mongoose from 'mongoose';

const Schema = mongoose.Schema;

const UserSchema = new Schema({
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  role: {
    type: String,
    enum: ['CHILD', 'PARENT'],
    default: 'CHILD',
  },
});
export default mongoose.model('user', UserSchema);
